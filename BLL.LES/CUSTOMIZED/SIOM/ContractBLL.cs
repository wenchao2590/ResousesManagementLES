using DAL.LES;
using DM.LES;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BLL.LES
{
    public class ContractBLL
    {
        #region Common
        ContractDAL dal = new ContractDAL();
        /// <summary>
        /// 分页获取集合
        /// </summary>
        /// <param name="textWhere">string 条件语句,无须where</param>
        /// <param name="textOrder">string 排序语句,无须order by</param>
        /// <param name="pageIndex">int 页码,从1开始</param>
        /// <param name="pageRow">int 每页行数</param>
        /// <param name="dataCount">out int 数据行数</param>
        /// <returns>List<ContractInfo></returns>
        public List<ContractInfo> GetListByPage(string textWhere, string textOrder, int pageIndex, int pageRow, out int dataCount)
        {
            dataCount = dal.GetCounts(textWhere);
            return dal.GetListByPage(textWhere, textOrder, pageIndex, pageRow);
        }
        /// <summary>
        /// SelectInfo
        /// </summary>
        /// <param name="id"></param>
        /// <returns></returns>
        public ContractInfo SelectInfo(long id)
        {
            return dal.GetInfo(id);
        }
        /// <summary>
        /// InsertInfo
        /// </summary>
        /// <param name="info"></param>
        /// <returns></returns>
        public long InsertInfo(ContractInfo info)
        {
            int cnt = dal.GetCounts("[CONTRACT_NO] = N'" + info.ContractNo + "'");
            if (cnt > 0)
                throw new Exception("MC:0x00000109");///合同编号不允许重复
            return dal.Add(info);
        }
        /// <summary>
        /// LogicDeleteInfo
        /// </summary>
        /// <param name="id"></param>
        /// <param name="loginUser"></param>
        /// <returns></returns>
        public bool LogicDeleteInfo(long id, string loginUser)
        {
            return dal.LogicDelete(id, loginUser) > 0 ? true : false;
        }
        /// <summary>
        /// UpdateInfo
        /// </summary>
        /// <param name="fields"></param>
        /// <param name="id"></param>
        /// <returns></returns>
        public bool UpdateInfo(string fields, long id)
        {
            string contractNo = CommonBLL.GetFieldValue(fields, "CONTRACT_NO");
            int cnt = dal.GetCounts("[ID] <> " + id + " and [CONTRACT_NO] = N'" + contractNo + "'");
            if (cnt > 0)
                throw new Exception("MC:0x00000109");///合同编号不允许重复
            return dal.UpdateInfo(fields, id) > 0 ? true : false;
        }

        #endregion
    }
}

